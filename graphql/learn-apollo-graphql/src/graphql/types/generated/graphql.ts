  numberOne?: Resolver<ResolversTypes['Int'], ParentType, ContextType>;
  numberToString?: Resolver<Maybe<ResolversTypes['String']>, ParentType, ContextType, RequireFields<QueryNumberToStringArgs, 'num'>>;
  numberTwo?: Resolver<ResolversTypes['Int'], ParentType, ContextType>;
  user?: Resolver<Maybe<ResolversTypes['User']>, ParentType, ContextType, RequireFields<QueryUserArgs, 'id'>>;
  users?: Resolver<Maybe<Array<Maybe<ResolversTypes['User']>>>, ParentType, ContextType>;
